            <div class="row">
                <div class="col-sm-4">
                    <img src="images/projets/cle.jpg" alt="Soutenir l’employeur dans l’inclusion professionnelle des personnes vivant avec une déficience intellectuelle ou un trouble du spectre de l’autisme (2016-2017)">
				</div>
				
                <div class="col-sm-8">

                    <p>Le Centre intégré de santé et de services sociaux de la Montérégie-Ouest (CISSSMO) offre des services d’adaptation, de réadaptation et d’intégration sociale aux personnes qui vivent avec une déficience intellectuelle (DI) ou un trouble du spectre de l’autisme (TSA). Pour ce faire, il leur fournit une gamme de services de soutien et d’accompagnement. Dans un souci d’offrir des services basés sur des données probantes et de faciliter l’implantation de meilleures pratiques, le CISSSMO a remis à jour son programme d’intégration et de réadaptation au travail. Afin de permettre à ses usagers de vivre une intégration au travail positive et valorisante, le CISSSMO constate que le soutien à l’employeur est primordial. Toutefois, la force de son expertise réside davantage dans l’identification des besoins et le soutien des personnes vivant avec une DI ou un TSA. Pour remédier à la situation, le CISSSMO bénéficiera de l’expertise du Centre de recherche pour l’inclusion scolaire et professionnelle des étudiants en situation de handicap (CRISPESH) afin de mieux répondre aux besoins des employeurs avec lesquels il collabore. Cela facilitera la mise en place d’environnements de travail inclusifs où les contextes qui mettraient leurs usagers en situation de handicap seraient réduits. L’obligation légale d’égalité à l’emploi au Québec se fonde sur le constat d’une discrimination systémique à l’endroit de certaines personnes, dont les personnes en situation de handicap (PSH). Plusieurs employeurs croient que celles-ci sont moins productives, ce qui engendre des pratiques discriminatoires. Pour contrer cette tendance, le monde de la gestion s’est donné un cadre d’opération vers un engagement planifié et systématique à recruter et maintenir en emploi des personnes ayant des profils (expériences, habiletés, etc.) diversifiés. Sous la bannière du concept de « gestion de la diversité », ce cadre vise l’équité et l’égalité des chances dans le monde du travail. Il s’agit de valoriser et reconnaître les différences individuelles en partant du postulat que chaque personne a le potentiel de contribuer à la performance d’une entreprise. Toutefois, ce cadre ne s’est pas encore généralisé dans les pratiques et au Québec, il comprend rarement les PSH.</p>
                    <p>Dans le cadre de ce projet, le CRISPESH et le CISSSMO uniront leurs efforts et leurs expertises pour développer des stratégies visant à favoriser la mise en place de pratiques de gestion de la diversité inclusive pour les PSH. En s’inspirant du concept d’inclusion, tel que défini en éducation, et du domaine de la gestion de la diversité, ce projet a pour objectif général le développement de bonnes pratiques de préparation et de soutien aux employeurs vers une gestion plus inclusive des personnes vivant avec une DI ou un TSA dans les milieux de travail. Les objectifs particuliers visent à élargir et à consolider les connaissances en matière de gestion de la diversité, de leadership d’inclusion et de changements de pratiques de gestion, en portant une attention particulière au handicap. Aussi, les chercheurs documenteront le point de vue de l’employeur sur les pratiques d’inclusion et d’intégration des personnes vivant avec une DI ou un TSA. Ce projet est novateur, puisqu’il compte développer un cadre de gestion de la diversité au Québec qui s’intéresse spécifiquement au handicap.</p>
                    <p>De façon générale, ce projet favorisera l’augmentation de la participation sociale des PSH grâce à une plus grande inclusion dans le monde professionnel. Il permettra au CISSSMO de mieux soutenir les employeurs avec lesquels il collabore, en plus de faciliter l’intégration des usagers. Les collèges et le CRISPESH innoveront et renforceront leur capacité de recherche et de transfert des connaissances grâce à une nouvelle expertise.</p>
					
